package com.rest.calculator.services;

public class MultiplyService {

	public int multiply(int a, int b) {
        return a * b;
    }
	public int plus(int a, int b) {
        return a + b;
    }
	public int moins(int a, int b) {
        return a - b;
    }
	public int divide(int a, int b) {
        return a / b;
    }
}
